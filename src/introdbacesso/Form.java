    private void PopularTabela(String sql){
        
             
            //Cria Conexao e guarda dados do banco em um resultSet.
            try{

          
            Conexao conexao = new Conexao();
            PreparedStatement pstm = null;
            ResultSet rs=null;
          
            pstm = conexao.con.prepareStatement(sql);
            rs = pstm.executeQuery();
          
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);  
          
          while(rs.next()){
              
              int i = 1;
                
                model.addRow(new Object[]
                {                
                    //Retorna dados da Tabels do BD, cada campo e coluna.
                      
                      rs.getString(1),
                      rs.getString(2),
                      rs.getString(3),
                      rs.getString(4),
                      rs.getString(5),
                      rs.getString(6)
  
                    });
                i++;
            }
          
           pstm.close();
           conexao.con.close();

              }catch(Exception e){
                  
                  JOptionPane.showMessageDialog(null, e.getMessage());
                  
              }
            finally{
            
            jTextField1.requestFocus();
            }
          
    }
    
